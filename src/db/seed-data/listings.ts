/**
 * Listing seed templates.
 *
 * One template = one canonical listing row. When `additionalChannels` is set,
 * the seeder creates extra raw_telegram_posts (one per additional channel)
 * and extra listing_sources rows pointing back to the same canonical
 * listing — this models "same property posted in multiple channels".
 *
 * Pricing rough rates (May 2026):
 *   1 USD ≈ 12,600 UZS
 *   Rent / month: 3-15M UZS or $300-1,500 in Tashkent
 *   Sale: 400M-5B UZS or $40K-500K
 *   Daily: 200K-1.5M UZS or $20-150
 *
 * Property images come from Unsplash. Some templates intentionally leave
 * mediaUrls empty so the UI's no-photo state is testable.
 *
 * Districts list comes verbatim from the master spec:
 *   Mirabad, Chilanzar, Yunusabad, Yakkasaray, Shaykhontohur, Sergeli, Almazar
 */

import type { Listing } from "../schema";

type PropertyType =
  | "apartment"
  | "house"
  | "commercial"
  | "land"
  | "room"
  | "studio";

type SeedListing = Omit<
  Listing,
  | "id"
  | "createdAt"
  | "updatedAt"
  | "categoryId"
  | "primaryRawPostId"
  | "duplicateGroupId"
  | "importedAt"
> & {
  /** Username from src/db/seed-data/channels.ts where this was posted */
  primaryChannel: string;
  /** Other channels where the same listing appeared. Adds listing_sources rows */
  additionalChannels?: string[];
  /** Unique seed key used to wire translations + duplicate groups */
  seedKey: string;
  /** If true, this listing is the canonical of a duplicate group; same group id is shared by all entries with this group tag */
  duplicateGroupTag?: string;
};

