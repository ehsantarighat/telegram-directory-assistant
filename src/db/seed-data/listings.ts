function pics(index: number, count = 2): string[] {
  return Array.from({ length: count }).map(
    (_, i) => UNSPLASH[(index + i) % UNSPLASH.length],
  );
}

/** Helper that builds a seed listing with sensible defaults. */
function L(
  partial: Partial<SeedListing> & Pick<SeedListing, "seedKey" | "title" | "listingType" | "primaryChannel" | "city" | "publishedAt">,
): SeedListing {
  return {
    summary: null,
    originalText: null,
    detectedLanguage: "ru",
    propertyType: "apartment" as PropertyType,
    country: "Uzbekistan",
    district: null,
    neighborhood: null,
    price: null,
    currency: null,
    rooms: null,
    areaSqm: null,
    floor: null,
    totalFloors: null,
    furnished: null,
    newBuilding: null,
    renovationStatus: null,
    metroNearby: null,
    ownerOrAgent: null,
    commission: null,
    parking: null,
    balcony: null,
    elevator: null,
    petsAllowed: null,
    heatingType: null,
    buildingMaterial: null,
    contactPhone: null,
    contactTelegram: null,
    hasPhotos: false,
    mainImageUrl: null,
    mediaUrls: [],
    sourceCount: 1,
    savedCount: 0,
    extractionConfidenceJson: {},
    status: "active",
    ...partial,
  } as SeedListing;
}

const now = Date.now();
function ago(daysAgo: number): Date {
  return new Date(now - daysAgo * 24 * 60 * 60 * 1000);
}

